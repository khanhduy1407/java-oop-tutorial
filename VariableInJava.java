public class VariableInJava {
    public static void main(String[] args) {
        String name;
        float height;
        int age;

        name = "NKDuy";
        height = 1.7f;
        age = 21;

        // c1
        System.out.println(name);
        System.out.println(height);
        System.out.println(age);
        System.out.println("=======================================");

        // c2
        System.out.print("Ten: ");
        System.out.println(name);
        System.out.print("Chieu cao: ");
        System.out.println(height);
        System.out.print("Tuoi: ");
        System.out.println(age);
        System.out.println("=======================================");
    }
}
